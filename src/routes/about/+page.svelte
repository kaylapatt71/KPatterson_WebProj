<h1>Welcome to The Rolling Pin Bakery.</h1>
<h1>A family-owned and operated bakery where every day begins with a sprinkle of love and a dash of passion</h1>
<p>Nestled in the heart of Dunedin, our bakery has been serving up delightful treats for generations. As a family, we believe that baking is not just an art, but a way to bring joy and comfort to our community.</p>
<p>Our journey started with Ann Goomes, who had a vision of creating a warm and inviting space where families could come together to enjoy freshly baked goodies. Today, that vision lives on as we continue to craft each confection with the same dedication and care that started it all.</p>
<p>At The Rolling Pin Bakery, we take pride in using only the finest, locally sourced ingredients. Our recipes are cherished family secrets, passed down from one generation to the next. From delectable pastries and cakes to mouthwatering cookies and bread, every bite is a testament to our commitment to quality.</p>

